package modelo;

public class Mesa {
	private int claveMesa;
	private String tipoMesa;
	private String statusMesa;
	
	public int getClaveMesa() {
		return claveMesa;
	}
	public void setClaveMesa(int claveMesa) {
		this.claveMesa = claveMesa;
	}
	public String getTipoMesa() {
		return tipoMesa;
	}
	public void setTipoMesa(String tipoMesa) {
		this.tipoMesa = tipoMesa;
	}
	public String getStatusMesa() {
		return statusMesa;
	}
	public void setStatusMesa(String statusMesa) {
		this.statusMesa = statusMesa;
	}
	
	
}
